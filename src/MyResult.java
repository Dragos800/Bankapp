class MyResult {
    private double first;
    private double second;
    private double third;

    public MyResult(double first, double second, double third) {
        this.first = first;
        this.second = second;
        this.third=third;
    }

    public double getFirst() {
        return first;
    }

    public double getSecond() {
        return second;
    }

    public double getThird() {
        return third;
    }
}